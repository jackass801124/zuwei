/**
 * 從命盤中提取命宮的所有星曜
 */
export function extractMingGongStars(chart: ChartResult): Star[] {
  const mingGongDizhi = chart.mingGongDizhi;
  const stars = (chart.starsByDizhi as any)[mingGongDizhi] || [];
  return stars;
}

/**
 * 生成單星解析
 */
export function generateSingleStarInterpretation(
  star: Star,
  mingGongDizhi: string,
  chart: ChartResult
): StarInterpretation {
  const baseInterpretation = SINGLE_STAR_LIBRARY[star.name]?.[(star.strength || 'default') as StarStrength | 'default'] 
    || SINGLE_STAR_LIBRARY[star.name]?.['default']
    || `${star.name}代表其特定的命理意義。`;

  let contextInterpretation = baseInterpretation;
  
  // 加入四化信息
  if (star.sihua) {
    const sihuaText = SIHUA_ENHANCEMENT[star.sihua as SiHuaType];
    contextInterpretation += `\n\n該星曜化${star.sihua}：${sihuaText}`;
  }

  return {
    starName: star.name,
    category: star.category,
    strength: star.strength,
    sihua: star.sihua,
    baseInterpretation,
    contextInterpretation,
  };
}

/**
 * 生成雙主星同宮複合解析
 */
export function generateDualMainStarAnalysis(
  mainStars: Star[],
  mingGongDizhi: string
): string {
  if (mainStars.length < 2) return '';

  const starNames = mainStars.map(s => s.name).sort().join('');
  const strengthKey = mainStars.map(s => `${s.name}${s.strength || '平'}`).join('');
  
  // 查詢雙主星組合
  const dualKey = Object.keys(DUAL_MAIN_STAR_LIBRARY).find(key => 
    key.includes(mainStars[0].name) && key.includes(mainStars[1].name)
  );

  if (dualKey) {
    const dualLibrary = DUAL_MAIN_STAR_LIBRARY[dualKey];
    return dualLibrary[strengthKey] || dualLibrary['default'] || '';
  }

  return '';
}

/**
 * 生成完整的命宮星曜深度解析
 */
export function generatePalaceInterpretation(chart: ChartResult): PalaceInterpretationResult {
  const mingGongDizhi = chart.mingGongDizhi;
  const allStars = extractMingGongStars(chart);

  const mainStars = allStars.filter(s => s.category === 'main');
  const luckyStars = allStars.filter(s => ['month', 'time', 'year'].includes(s.category) && 
    ['左輔', '右弼', '文昌', '文曲', '祿存', '天馬'].includes(s.name));
  const unluckyStars = allStars.filter(s => ['month', 'time', 'year'].includes(s.category) && 
    ['擎羊', '陀羅', '火星', '鈴星', '地空', '地劫'].includes(s.name));
  const minorStars = allStars.filter(s => !mainStars.includes(s) && !luckyStars.includes(s) && !unluckyStars.includes(s));

  const mainStarInterpretations = mainStars.map(s => generateSingleStarInterpretation(s, mingGongDizhi, chart));
  const luckyStarInterpretations = luckyStars.map(s => generateSingleStarInterpretation(s, mingGongDizhi, chart));
  const unluckyStarInterpretations = unluckyStars.map(s => generateSingleStarInterpretation(s, mingGongDizhi, chart));
  const minorStarInterpretations = minorStars.map(s => generateSingleStarInterpretation(s, mingGongDizhi, chart));

  const combinationAnalysis = generateDualMainStarAnalysis(mainStars, mingGongDizhi);

  // 生成整體性格特質
  const overallCharacter = mainStars.length > 0
    ? `命宮主星為${mainStars.map(s => s.name).join('、')}，${combinationAnalysis || '性格複雜多面，需要根據具體星曜組合進行深入分析。'}`
    : '命宮無主星，性格較為平穩，容易受外界影響。';

  // 生成人生路徑警示
  const hasUnluckyStar = unluckyStars.length > 0;
  const hasSihuaJi = allStars.some(s => s.sihua === '忌');
  let lifePathWarning = '命盤吉星眾多，運勢良好。';
  if (hasUnluckyStar || hasSihuaJi) {
    lifePathWarning = '命盤中存在煞星或化忌，需要特別注意。建議加強自律與理性判斷，避免陷入困境。';
  }

  return {
    mingGongDizhi,
    mainStars: mainStarInterpretations,
    luckyStars: luckyStarInterpretations,
    unluckyStars: unluckyStarInterpretations,
    minorStars: minorStarInterpretations,
    combinationAnalysis,
    overallCharacter,
    lifePathWarning,
  };
}
